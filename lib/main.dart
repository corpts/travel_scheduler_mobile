import 'package:flutter/material.dart';
import 'package:flutter/services.dart';


import 'pages/login/start_page.dart';
import 'pages/login/sign_up.dart';
import 'pages/login/sign_up_password.dart';
import 'pages/login/sing_in.dart';


void main() {
  runApp(MyApp());
}

class MyApp extends StatelessWidget {
  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    SystemChrome.setEnabledSystemUIOverlays ([]);
    return MaterialApp(
      title: 'Flutter Demo',
      initialRoute: '/login_start',
      routes: {
        '/' : (context) => MyHomePage(title: 'Flutter Demo Home Page'),
        '/login_start': (context) => StartPage(),
        '/sign_up': (context) => SignUpPage(),
        '/sign_up_password': (context) => SignUpPasswordPage(),
        '/sign_in': (context) => SignInPage()
      }
    );
  }
}

class MyHomePage extends StatefulWidget {
  MyHomePage({Key key, this.title}) : super(key: key);
  final String title;

  @override
  _MyHomePageState createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  int _counter = 0;

  void _incrementCounter() {
    setState(() {
      _counter++;
    });
  }

  @override
  Widget build(BuildContext context) {

    return Scaffold(
      appBar: AppBar(
        title: Text(widget.title),
      ),
      body: Center(
        child: Column(

          mainAxisAlignment: MainAxisAlignment.center,
          children: <Widget>[
            Text(
              'You have pushed the button this many times:',
            ),
            Text(
              '$_counter',
              style: Theme.of(context).textTheme.headline4,
            ),
          ],
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _incrementCounter,
        tooltip: 'Increment',
        child: Icon(Icons.add),
      ),
    );
  }
}
